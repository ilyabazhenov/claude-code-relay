#!/usr/bin/env bash
#
# Build Relay and assemble it into a proper macOS .app bundle.
#
# Usage:
#   scripts/build_app.sh [debug|release]
#
# Produces: build/Relay.app
#
# The bundle is ad-hoc code-signed so that UNUserNotificationCenter works when the
# app is launched locally. For distribution see README (signing / notarization).

set -euo pipefail

CONFIG="${1:-debug}"
ROOT="$(cd "$(dirname "$0")/.." && pwd)"
cd "$ROOT"

APP_NAME="Relay"
BUNDLE_ID="com.relay.menubar"
OUT_DIR="$ROOT/build"
APP_DIR="$OUT_DIR/$APP_NAME.app"

echo "==> swift build -c $CONFIG"
swift build -c "$CONFIG"

BIN_PATH="$(swift build -c "$CONFIG" --show-bin-path)/$APP_NAME"
if [[ ! -f "$BIN_PATH" ]]; then
  echo "error: built binary not found at $BIN_PATH" >&2
  exit 1
fi

echo "==> assembling $APP_DIR"
rm -rf "$APP_DIR"
mkdir -p "$APP_DIR/Contents/MacOS"
mkdir -p "$APP_DIR/Contents/Resources"

cp "$BIN_PATH" "$APP_DIR/Contents/MacOS/$APP_NAME"
cp "$ROOT/Resources/Info.plist" "$APP_DIR/Contents/Info.plist"
printf 'APPL????' > "$APP_DIR/Contents/PkgInfo"

# --- Version stamping -------------------------------------------------------------
# CFBundleShortVersionString comes from the VERSION file (the human-facing marketing
# version). CFBundleVersion is the monotonic build number Sparkle compares releases by;
# we derive it from the git commit count so it always increases. Both are written into
# the copied Info.plist so the source plist stays a static template.
PLIST="$APP_DIR/Contents/Info.plist"
SHORT_VERSION="$(tr -d ' \n' < "$ROOT/VERSION" 2>/dev/null || echo "0.0.0")"
BUILD_NUMBER="$(git -C "$ROOT" rev-list --count HEAD 2>/dev/null || echo "1")"
PB=/usr/libexec/PlistBuddy
"$PB" -c "Set :CFBundleShortVersionString $SHORT_VERSION" "$PLIST"
"$PB" -c "Set :CFBundleVersion $BUILD_NUMBER" "$PLIST"
echo "==> version $SHORT_VERSION (build $BUILD_NUMBER)"

# App icon (Finder / Get Info / notification banners). Generated by make_appicon.sh.
if [[ -f "$ROOT/Resources/AppIcon.icns" ]]; then
  cp "$ROOT/Resources/AppIcon.icns" "$APP_DIR/Contents/Resources/AppIcon.icns"
else
  echo "note: Resources/AppIcon.icns missing — run scripts/make_appicon.sh to add it" >&2
fi

# --- Embed Sparkle.framework ------------------------------------------------------
# Sparkle ships as a binary artifact resolved by SwiftPM under .build/artifacts. Since we
# assemble the bundle by hand (no Xcode "Embed Frameworks" phase), copy the framework into
# Contents/Frameworks ourselves. The executable carries an rpath of
# @executable_path/../Frameworks (see Package.swift) so dyld finds it there.
FRAMEWORKS_DIR="$APP_DIR/Contents/Frameworks"
SPARKLE_FW="$(find "$ROOT/.build/artifacts" -type d -name 'Sparkle.framework' -path '*macos*' 2>/dev/null | head -1)"
if [[ -z "$SPARKLE_FW" ]]; then
  echo "error: Sparkle.framework not found under .build/artifacts — run 'swift build' first" >&2
  exit 1
fi
echo "==> embedding Sparkle.framework"
mkdir -p "$FRAMEWORKS_DIR"
rm -rf "$FRAMEWORKS_DIR/Sparkle.framework"
cp -R "$SPARKLE_FW" "$FRAMEWORKS_DIR/Sparkle.framework"
SPK="$FRAMEWORKS_DIR/Sparkle.framework"

# Ad-hoc code signing, strictly inside-out: nested helpers and XPC services first, then
# the framework, then (below) the app. Signing the app last seals the framework by its
# cdhash; a stale inner signature would fail Sparkle's own validation at update time.
echo "==> ad-hoc signing Sparkle (inside-out)"
codesign --force --sign - "$SPK/Versions/Current/XPCServices/Downloader.xpc"
codesign --force --sign - "$SPK/Versions/Current/XPCServices/Installer.xpc"
codesign --force --sign - "$SPK/Versions/Current/Updater.app"
codesign --force --sign - "$SPK/Versions/Current/Autoupdate"
codesign --force --sign - "$SPK"

echo "==> ad-hoc code signing app"
codesign --force --sign - --identifier "$BUNDLE_ID" "$APP_DIR" >/dev/null 2>&1 || {
  echo "warning: codesign failed; notifications may not work" >&2
}

# Verify the app's seal (and thus the embedded framework) is consistent.
codesign --verify --deep --strict "$APP_DIR" 2>&1 | sed 's/^/note: /' || \
  echo "warning: codesign --verify reported issues; the update helpers may not launch" >&2

# Re-register with LaunchServices. Rebuilding in place gives the bundle a new ad-hoc
# signature; without this, a stale registration points at the old cdhash and the app
# aborts on launch inside +[UNUserNotificationCenter currentNotificationCenter]
# ("bundleProxyForCurrentProcess is nil"). Forcing re-registration keeps launches clean.
LSREGISTER="/System/Library/Frameworks/CoreServices.framework/Versions/A/Frameworks/LaunchServices.framework/Versions/A/Support/lsregister"
if [[ -x "$LSREGISTER" ]]; then
  "$LSREGISTER" -f "$APP_DIR" >/dev/null 2>&1 || echo "note: lsregister failed; if the app aborts on launch, run it manually" >&2
fi

echo "==> done: $APP_DIR"
echo
echo "Run it with:"
echo "  open \"$APP_DIR\""
echo "or (to see logs in the terminal):"
echo "  \"$APP_DIR/Contents/MacOS/$APP_NAME\""
